vector<string> split(const string &str) {
    vector<string> tokens;

    string::size_type start = 0;
    string::size_type end = 0;

    while ((end = str.find(" ", start)) != string::npos) {
        tokens.push_back(str.substr(start, end - start));

        start = end + 1;
    }

    tokens.push_back(str.substr(start));

    return tokens;
}

string ltrim(const string &str) {
    string s(str);

    s.erase(
            s.begin(),
            find_if(s.begin(), s.end(), not1(ptr_fun<int, int>(isspace)))
    );

    return s;
}

string rtrim(const string &str) {
    string s(str);

    s.erase(
            find_if(s.rbegin(), s.rend(), not1(ptr_fun<int, int>(isspace))).base(),
            s.end()
    );

    return s;
}
//Slice vector into given dimensions along with step
V<int> vec_slice(V<int> &data, int start, int end, int step) {
    V<int> res;
    for (int i=start; i<end; i+=step){
        res.push_back(data.at(i));
    }
    return res;
}

string gamingArray(vector<int> arr) {
    int counter=0;
    while (!arr.empty()){
        arr = V<int>(arr.begin(), max_element(arr.begin(), arr.end()));
        counter+=1;
    }

    if (counter%2==0){
        return "ANDY";
    } else {
        return "BOB";
    }

}

